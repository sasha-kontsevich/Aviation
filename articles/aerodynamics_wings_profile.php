<h2>1. Консольные монопланы</h2>
<div class="row">
    <p>
        К концу 20-х годов прошлого века конструкторы пришли к выводу, что лес
        расчалок и растяжек, требовавшихся для бипланов, не позволяет увеличить
        скорость самолета. Даже самые быстрые машины (такие, как гидроплан
        SchneiderTrophy) имели их в большом количестве. Одним из первых инженеров,
        отказавшихся от использования расчалок и растяжек, был голландец
        Энтони Фоккер, разработавший консольный моноплан. Фоккер утверждал,
        что соотношение t/c крыла должно составлять 20 процентов. Соотношение
        t/с-это ключевой показатель в конструкции крыла, указывающий отношение
        толщины крыла (в самом толстом его сечении) к хорде профиля (расстоянию
        между ведущей и задней кромкой крыла в той же плоскости сечения).
    </p>
</div>
