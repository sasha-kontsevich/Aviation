<h2>З. Толщина крыла</h2>
<div class="row">
    <p>
        В 1 937 году инженер Сидней Кэмм, главный
        конструктор фирмы «Hawker», начал работу над
        самолетами Tornado и Турhооп. Их предполагалось
        оснастить двигателями мощностью 2000 л. с., что
        позволило бы развивать скорость до 644 км/ч.
        Пилоты, бросавшие новые самолеты в крутое пике,
        обнаружили, что они при этом сильно вибрируют.
        Иногда это приводило даже к потере контроля над
        управлением.
        Стало ясно, что в аэродинамической схеме самолета
        допущена какая-то существенная ошибка.
        Со временем, после многочисленных происшествий
        в воздухе с этими и другими скоростными
        истребителями, ученые обнаружили, что при
        крутом пикировании скорость воздушного потока
        на верхней кромке крыла достигает скорости звука.
        Это и приводило к возникновению ударных волн
        (о них будет сказано ниже). Решить проблему могли
        только еще более тонкие крылья, которые должны
        были стать гораздо прочнее и, следовательно,
        тяжелее.
        Если соотношение t/c у самолета Typhoon превышало
        18 процентов, то у Tempest оно составляло от
        1 4.5 (у корня крыла) до 1Оо/о1у его за концовки). Поэтому
        Tempest, оснащенный тем же двигателем, был
        хотя и тяжелее, чем Typhoon, но быстрее на 48 км/ч.
        А. что еще более важно, обтекающий крыло воздушный
        поток не разрушался, поэтому летчик мог вести
        воздушный бой, не рискуя попасть в тряску </p>
</div>
