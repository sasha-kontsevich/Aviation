<h2>5. Завихрения</h2>
<div class="row">
    <p>
        Давление воздушного потока на верхней части крыла меньше, чем на его
        нижней части. У законцовок крыла нижний поток воздуха имеет тенденцию
        подниматься вверх, в область более низкого давления над крылом, в результате
        чего возникают сильные спиральные завихрения.
    </p>
</div>
<div class="row">
    <div class="image-wrapper">
        <img src="images/aerodynamics_wings_profile_6.jpg" alt='Hawker Tempest VI'>
        <!-- <div class="description">Hawker Tempest VI</div> -->
    </div>
</div>

<div class="row">
    <p>
        След завихрения тянется позади за концовок крыла и может сохраняться
        на протяжении нескольких минут. При резких маневрах низкое давление
        внутри завихренного воздушного потока может приводить к конденсации
        атмосферной влаги. поэтому за кончиками крыльев образуется
        длинный белый шлейф.
    </p>
</div>
<h2>6. Использование крылышек-винглетов</h2>
<div class="row">
    <p>
        Если маленький самолет садится вслед за большим (даже через несколько
        минут) и попадает в оставшиеся воздушные завихрения, то он может потерять
        управление и упасть на землю. Поэтому существуют строгие правила
        относительно интервала между посадкой самолетов.
        В конце 1 960-х годов конструкторы начали экспериментировать с законцовками
        крыла, добавляя на них винглеты (иногда их называют аэродинамическими
        перегородками, концевыми шайбами). Они препятствуют
        перетеканию воздуха через за концовку, что уменьшает сопротивление,
        увеличивает эффективную площадь крыла и несколько облегчает движение
        в турбулентной атмосфере. </p>
</div>
