<h1>Аэродинамика. Профили и толщина крыла</h1>

<div class="row">
    <div class="preamble">
        После того как была создана основная концепция крыла, инженеры и конструкторы принялись улучшать его. Развитие авиации шло стремительно. Нескольких лет хватало для внесения новых революционных изменений в конструкцию аэропланов. В свое время появился моноплан, а вместе с ним перед конструкторами самолетов возникли и новые проблемы . </div>
