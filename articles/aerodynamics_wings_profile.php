<h2>2. Дюралюминий</h2>
<div class="row">
    <p>
        Конструкторы в Германии и США учились использовать новый алюминиевый
        сплав, названный дюралюминием, который был легким, как алюминий,
        но намного прочнее его. Он открыл дорогу к созданию консольных монопланов
        с крылом, не требовавшим такого высокого отношения t/c, как у самолетов
        с деревянной конструкцией. Это привело к созданию скоростных
        машин с более тонким крылом (как, например, у DC-1 ).
    </p>
