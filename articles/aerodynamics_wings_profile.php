<h2>4. Профиль ламинарного потока</h2>
<div class="row">
    <p>
        Было еще одно важное различие между крыльями самолетов
        Typhoon и Tempest. Толщина крыла Typhoon достигала максимума
        неподапеку от ведущей кромки, поэтому на передней части крыла
        воздушный поток стремительно ускорялся. В конце 1 930-х годов работники
        Национального консультативного комитета по аэронавтике
        США пришли к открытию так называемого профиля nаминарного
        потока. Максимальная толщина сечения была передвинута в точку,
        расположенную в 35-40 процентах расстояния от переднего края
        хорды. Это позволило воздушному потоку ускоряться на значительно
        большей по размеру части крыла. Скорость воздушного потока возрастала,
        градиент давления был удовлетворительным. Иными словами,
        воздух двигался с ускорением, что повышало устойчивость потока
        к возмущениям и «прикрепляло» к крылу воздушный поток. Когда
        скорость потока начинала снижаться, градиент давления становился
        неблагоприятным (поток начинал отделяться от поверхности крыла,
        принимая характер турбулентного). Одним из первых самолетов
        с крылом ламинарного профиля был North American Mustang. Оснащенный
        тем же двигателем, что и Spitfire IX, он развивал скорость на
        55 км/ч больше, несмотря на то, что был тяжелее и втрое превосходил
        Spitfire по объему бака для горючего. Справа показаны профили
        сечения крыла Турhооп (а) и Mustang (Ь). Профиль крыла Mustang
        был аналогичен профилю крыла Tempest.
    </p>
