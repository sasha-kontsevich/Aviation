<h1>Авионика. Введение</h1>
<div class="row">
    <div class="preamble">
        Все современные самолеты, от простейших легкомоторных
        машин до истребителей и бомбардировщиков, оснащены
        авионикой, или, как принято называть в России,
        бортовым радиоэлектронным оборудованием (БРЭО),
        призванным облегчить работу экипажа.
    </div>
</div>

<div class="row">
    <p>
        Термин «авионика» относительно
        новый - пионеры
        авиации управляли своими
        аэропланами исключительно с помощью
        механических устройств.
        Однако дальнейшее развитие
        авиатехники стало невозможным
        без внедрения электроники -
        особенно бурное развитие этот
        процесс получип в 1960-х годах,
        а в конце ХХ века произошла
        настоящая революция, связанная
        с появлением электродистанционных
        систем управления (ЭДСУ). </p>
</div>

<h2>Военная авионика</h2>
<div class="row">
    <p>
        Первые попытки внедрения
        электроники на летательные
        аппараты предпринимались задолго
        до 1 960-х годов. Например,
        в 1 928 году в США пассажирский
        самолет впервые выполнил заход
        и посадку с использованием инструментапьной
        системы посадки,
        основанной на использовании
        приводного радиомаяка и направленной антенны. В 1930 году немецкие
        инженеры разработали радионавигационную
        систему (РНС)
        на основе двух радиомаяков, зоны
        действия которых перекрывались.
        Она обеспечивала довопьно точное
        самолетовождение ночью и
        в плохую погоду. На самолете монтировался
        приемник с динамиком,
        который в случае выхода летчика
        из «равносигнальной зоны» (зона,
        где сигналы радиомаяков гасили
        друг друга) выдавал акустический
        сигнал. Серийно первые РНС
        выпускала фирма «Lorenz», они
        послужили прообразом для послевоенных
        радиосистем ближней навигации,
        известных на Западе как
        VOR (гражданский вариант) или
        TACAN (для военных), а в нашей
        стране известных как РСБН. </p>
</div>
