<div class="row">
    <p>
        Вторая мировая война стимулировала
        развитие авионики. На
        самолетах появились первые РЛС,
        а в Германии успешно продолжапись
        работы по РНС - созданы
        и использованы на практике
        системы, обеспечивающие бомбометание
        по цели вне ее видимости,
        по радиосигналам. Обе воюющие
        стороны также вели активные
        работы по созданию систем радиоэлектронного
        противодействия
        (борьбы).
    </p>
    <p>
        Вне всяких сомнений, одними
        из самых сложных электронных
        устройств того периода стапи построенные
        на основе гироскопов
        системы управления немецких ракет
        «Фау- 1 » и «Фау-2». Появились
        и первые системы «Свой-чужой».
        В итоге к 1 945 году на бомбардировщике
        В-29 имелось порядка
        2000 электронных устройств, хотя
        буквально через несколько лет на
        В-52 электронных «гаджетов» было
        уже более 50 тысяч.
    </p>
    <p>
        Создание новых баллистических
        ракет и полет человека в космос
        дали новый толчок развитию
        эпектроники. Миниатюризация
        эпектронных устройств позвонила
        СССР вывести на орбиту пилотируемый
        космический корабль, а США - осуществить высадку
        человека на Луну.
    </p>
</div>
