<div class="row">
    <p>
        Большую часть стоимости истребителей
        5-го поколения, таких
        как американские F-22 Raptor и
        F-35 Lightпing 11 или российский
        Т-50, составляет стоимости электроники.
        На современных самолетах
        информация от множества
        датчиков и систем выводится на
        нашлемные дисплеи летчиков,
        удалось реализовать и концепцию
        «глаза вне кабины». Летчик с помощью
        цифровых устройств может
        наблюдать все пространство
        вокруг самолета - информация
        синтезируется на основе данных
        от электронно-оптических датчиков,
        РЛС и систем бортового комплекса
        обороны. </p>
</div>
<h2>Гражданское применение</h2>
<div class="row">
    <p>
        Со временем электроника нашла
        широкое применение и в гражданской авиации, причем зачастую
        военные разработки затем активно
        внедрялись на гражданских самолетах.
        Так, например, на Западе
        ЭДСУ впервые была установлена
        на истребителях F- 1 6 и F/A- 18,
        но затем быстро появилась и на
        гражданских авиалайнерах -
        первенцем стал Airbus А320. Хотя
        первым в мире самолетом с ЭДСУ
        является опытный ракетоносец
        Т-4, спроектированный в ОКБ
        Сухого. Для военных вертолетов
        были разработаны системы автостабилизации
        - сегодня они
        устанавливаются практически на
        все вертолеты вне зависимости от
        назначения.
    </p>
</div>
