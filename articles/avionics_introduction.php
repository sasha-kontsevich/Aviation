<div class="row">
    <p>
        Естественно, что преимущества
        электроники использовались,
        в первую очередь, в военных
        целях, и нигде в мире военные
        не придавали электронике столь
        большого значения, как в США.
        Вплоть до 1980-х годов расходы
        Пентагона в области электроники
        почти не ограничивались, что
        позволило США в этой сфере
        обогнать СССР на многие годы.
        К примеру, линия (интерфейс)
        передачи данных был разработан
        в 1960-е годы для облегчения наведения
        самолетов-перехватчиков,
        но в полной мере ее возможности
        удалось раскрыть только в середине
        1980-х годов в американской
        единой системе обмена тактической
        информацией (JТIDS),
        полноценных аналогов которой
        в России нет до сих пор. Система
        JTIDS позволяет самолетам обмениваться
        информацией в режиме
        реального времени между собой
        и с другими боевыми единицами,
        вплоть до командира отдельного
        наземного подразделения (в определенной
        мере это «военный
        интернет»). В кабине самолета
        информация отображается на
        дисплее с движущейся цифровой
        картой местности. Внедрение
        в войска спутниковой навигационной
        системы (СНС) существенно
        повысило точность определения
        своего местоположения и координат
        различных объектов и целей,
        а создание построенных на основе
        гироскопов инерциальных навигационных
        систем (ИНС) позволило
        резко повысить точность самолетовождения
        в автономном режиме,
        без коррекции по сне и иным
        радиотехническим средствам.
    </p>
</div>

