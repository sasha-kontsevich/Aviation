<div class="row">
    <p>
        В гражданской авиации большое
        внимание уделялось автоматизации
        процессов управления
        полетом. Первая в мире полностью
        автоматическая система
        посадки была опробована в марте
        1964 года на самолете ВЕЛ Trident,
        но сегодня подобные системы являются
        штатными и даже позволяют
        выполнять в автоматическом
        режиме руление по аэродрому.
        Другой важнейшей инновацией
        стала система предупреждения
        об опасном сближении самолетов
        в воздухе (TCAS), она не только
        предупреждает пилотов об опасной
        близости другого самолета,
        но и выдает рекомендации для
        снижения вероятности столкновения.
        Внедрение на самолеты
        оборудования сне и прогресс
        традиционных средств навигации
        значительно повысили точность
        самолетовождения и безопасность
        полетов. Разработана процедура
        PBN (performaпce-based
        navigation), позволяющая выполнять
        не только безопасный полет
        и экономить топливо, но и совершать
        безопасную посадку без
        использования радиотехнических
        систем посадки даже на аэродромы,
        расположенные в горах.
        Электроника во многом определила
        развитие интерфейса «человек
        - машина». Первые индикаторы
        на основе электронно-лучевых
        трубок появились сначала на
        военных, а затем и на гражданских
        самолетах. Впоследствии им на
        смену пришли многофункциональные
        жидкокристаллические
        индикаторы (ЖКИ). Первым
        авиалайнером, на котором реализовали
        концепцию «стеклянной
        кабины», стал Boeiлg 767. Его приборное
        оборудование в 1981 году
        выполнили по аналогии с самолетами
        F/A- 1 8 и F- 15. В настоящее
        время приборное оборудование
        на основе многофункциональных
        ЖКИ ставится даже на легкомоторные
        самолеты авиации общего
        назначения. </p>
