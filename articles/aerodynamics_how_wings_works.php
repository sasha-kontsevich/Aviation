<h2>7. Опыты Бернулли</h2>
<div class="row">
    <p>
        Почти 300 лет назад Даниил Бернулли открыл. что идеальная несжимаемая жидкость
        или газ (воздух не является полностью «идеальным• газом, поскольку его
        молекулы имеют тенденцию «слипаться• друг с другом) в каждой точке своего
        потока обладает постоянным давлением. которое складывается из внешнего,
        статического и динамического давления. Закон Бернулли стал одним из вариантов
        фундаментального закона сохранения энергии.
    </p>
</div>
<div class="row">
    <div class="image-wrapper">
        <img src="images/aerodynamics_how_wings_works_7.jpg" alt='Опыты Бернулли'>
        <div class="description">Манометры, показывающие давnение воздуха в разnичных точках трубы</div>
    </div>
</div>
<div class="row">
    <p>
        Представьте себе движение воздуха в трубе. которая сужается к точке с минимальным
        поперечным сечением (горло), а затем вновь расширяется. Такая
        труба называется трубой Вентури. Что происходит при этом с давлением? Ответ
        кажется очевидным: давление воздуха максимально увеличивается в горловине,
        а затем вновь нормализуется. На самом деле, как показал Бернулли, происходит
        нечто совершенно противоположное. Когда воздушный поток проходит
        внутри трубы, его скорость (отмечаемая на круглых шкалах) возрастает до
        максимума в горловине, а затем возвращается к норме. Поскольку количество
        энергии остается неизменным. давление в горловине падает до минимума, а
        затем возвращается к прежним показателям. На фотографии вверху вы видите
        такую трубу на фоне кабины самолета. Применяется для измерения скорости
        потока за счет изменения перепада давления.
    </p>
</div>
<h2>8. Суммарная подъемная сила</h2>
<div class="row">
    <p>
        Теперь представьте трубу Вентури разрезанной и раскатанной так. что она
        образует наружную поверхность крыла. Когда воздушный поток обтекает трубу,
        его давление падает. а давление воздуха снизу толкает крыло вверх. То, что при
        обтекании крыла с большой скоростью давление в воздушном потоке не увеличивается,
        а падает. доказывает простой эксперимент с листом бумаги. Детали
        такого эксперимента были впервые опубликованы в 1910 году.
    </p>
