<h2>6. Как работает крыло</h2>
<div class="row">
    <p>
        В эпоху бипланов конструкторы забыли о том, как работают настоящие аэродинамические
        секции крыла, отличающегося от простой наклонной плоскости.
        Ключевым фактором является то, что воздух обтекает верхнюю выпуклую (и более
        длинную) поверхность крыла быстрее, чем его почти плоскую нижнюю поверхность.
        Чтобы составить графическую схему обтекания крыла, специалисты
        по аэродинамике назвали путь, проделанный каждой молекулой воздуха, линией
        воздушного потока. В невозмущенном воздушном потоке эти линии параллельны,
        как показано слева. В области крыла ситуация кардинально меняется.
    </p>
